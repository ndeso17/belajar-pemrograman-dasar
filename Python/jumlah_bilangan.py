total = 0 #? variabel total untuk nilai awal
for i in range(1, 101): #? perulangan angka 1-100
    total += i #? memperbarui variabel total yang telah didefinisikan diatas dengan nilai baru penjumlahan dari for.
print("Jumlah semua bilangan dari 1 hingga 100 adalah:", total) #? menampilkan hasil dengan keterangan
